package design.patterns.command.workshop;

public class Robot {

    public void turnOn() {
        System.out.println("Robot started!");
    }

    public void cut() {
        System.out.println("Cut!");
    }

    public void drill() {
        System.out.println("Drill!");
    }

    public void turnOff() {
        System.out.println("Robot turned off!");
    }

}
